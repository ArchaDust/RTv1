#include "libft.h"

char		*ft_strtrim(char const *s)
{
	int		i;
	int		len;
	char	*res;

	if (s == NULL)
		return (NULL);
	len = ft_strlen(s);
	while (s[len - 1] == ' ' || s[len - 1] == '\t' || s[len - 1] == '\n')
		len--;
	i = -1;
	while (s[++i] == ' ' || s[i] == '\t' || s[i] == '\n')
		len--;
	if (len <= 0)
		len = 0;
	res = (char*)malloc(len + 1);
	if (res == NULL)
		return (NULL);
	s += i;
	i = -1;
	while (++i < len)
		res[i] = *s++;
	res[i] = '\0';
	return (res);
}
